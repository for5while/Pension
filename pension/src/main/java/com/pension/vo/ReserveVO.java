package com.pension.vo;

public class ReserveVO {
	private int year;
	private int month;
	private int day;
	private int idx;
	private int optionIndex;
	private String optionName;
	private int optionPrice;
	private int night;
	private int peopleAdult;
	private int peopleChild;
	private int peopleInfant;
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getOptionIndex() {
		return optionIndex;
	}
	public void setOptionIndex(int optionIndex) {
		this.optionIndex = optionIndex;
	}
	public String getOptionName() {
		return optionName;
	}
	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}
	public int getOptionPrice() {
		return optionPrice;
	}
	public void setOptionPrice(int optionPrice) {
		this.optionPrice = optionPrice;
	}
	public int getNight() {
		return night;
	}
	public void setNight(int night) {
		this.night = night;
	}
	public int getPeopleAdult() {
		return peopleAdult;
	}
	public void setPeopleAdult(int peopleAdult) {
		this.peopleAdult = peopleAdult;
	}
	public int getPeopleChild() {
		return peopleChild;
	}
	public void setPeopleChild(int peopleChild) {
		this.peopleChild = peopleChild;
	}
	public int getPeopleInfant() {
		return peopleInfant;
	}
	public void setPeopleInfant(int peopleInfant) {
		this.peopleInfant = peopleInfant;
	}
}
